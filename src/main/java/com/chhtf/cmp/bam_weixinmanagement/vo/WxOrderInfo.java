package com.chhtf.cmp.bam_weixinmanagement.vo;

public class WxOrderInfo {
	public String openid;
	public String globalOrderNo;
	public String totalFee;
	public String getOpenid() {
		return openid;
	}
	public void setOpenid(String openid) {
		this.openid = openid;
	}
	public String getGlobalOrderNo() {
		return globalOrderNo;
	}
	public void setGlobalOrderNo(String globalOrderNo) {
		this.globalOrderNo = globalOrderNo;
	}
	public String getTotalFee() {
		return totalFee;
	}
	public void setTotalFee(String totalFee) {
		this.totalFee = totalFee;
	}
}
